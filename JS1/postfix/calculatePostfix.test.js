const calculatePostfix = require("./calculatePostfix.js");
test("add ['100','10','6','*','+','50','+'] to be 210", () => {
  expect(calculatePostfix(["100", "10", "6", "*", "+", "50", "+"])).toBe(210);
});
test("add ['10','2','+','3','15','5','/','*','-','100','+'] toBe 103", () => {
  expect(
    calculatePostfix([
      "10",
      "2",
      "+",
      "3",
      "15",
      "5",
      "/",
      "*",
      "-",
      "100",
      "+",
    ])
  ).toBe(103);
});
test("add ['6','5','3','2','^','*','+','4','2','*','4','2','-','/','-','20','+'] toBe 67", () => {
  expect(
    calculatePostfix([
      "6",
      "5",
      "3",
      "2",
      "^",
      "*",
      "+",
      "4",
      "2",
      "*",
      "4",
      "2",
      "-",
      "/",
      "-",
      "20",
      "+",
    ])
  ).toBe(67);
});
